#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView
{
    // Return the number of sections.
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section
{
    // Return the number of rows in the section.
    return self.commons.count;
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"commonName" forIndexPath:indexPath];
    
    // Configure the cell...
    NSDictionary *common = self.commons[indexPath.row];
    cell.textLabel.text = common[@"name"];
    
    if ([common[@"shouldDisplay"] boolValue]) {
        cell.accessoryType = UITableViewCellAccessoryCheckmark;
    } else {
        cell.accessoryType = UITableViewCellAccessoryNone;
    }
    
    return cell;
}

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath {
    UITableViewCell *cell = [self.tableView cellForRowAtIndexPath:indexPath];
    
    NSMutableDictionary *common = [self.commons[indexPath.row] mutableCopy];
    
    if (cell.accessoryType == UITableViewCellAccessoryCheckmark) {
        cell.accessoryType = UITableViewCellAccessoryNone;
        common[@"shouldDisplay"] = [NSNumber numberWithBool:NO];
    } else {
        cell.accessoryType = UITableViewCellAccessoryCheckmark;
        common[@"shouldDisplay"] = @YES;
    }
    
    self.commons[indexPath.row] = [NSDictionary dictionaryWithDictionary:common];
}

